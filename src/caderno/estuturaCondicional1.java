package caderno;

public class estuturaCondicional1 {

    public static void main(String[] args) {
        
        // vamos usar o exemplo if para mostrar na prática.

        int x = 5; // primeiro criamos uma variavel inteira x e atribuimos o valor 5 a ela 

        System.out.println("Bom dia");
        
        if (x < 0) {     // aqui estamos criando uma estrutura condicional, onde se x for menor que 0, o código dentro do bloco if será executado.
            System.out.println("Boa tarde");
        }
        
        System.out.println("Boa noite");


    }

}
